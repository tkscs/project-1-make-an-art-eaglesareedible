from turtle import *
import numpy as np
speed(0)
def sideface(b):
    penup()
    setposition(0, 100)
    pendown()
    setheading(180)
    circle(50, b*90)
    right(b*60)
def draweye(side):
    setheading(0)
    penup()
    setposition(-25*side, 0)
    pendown()
    begin_fill()
    circle(10)
    end_fill()
def drawear(b):
    penup()
    setposition(0, 100)
    setheading(90)
    left(90)
    circle(50, b*45)
    if b == 1:
        setheading(180)
    else:
        setheading(0)
    pendown()
    for i in range(13):
        forward(10)
        left(b*90/8)

